package ej8arrays;

/**
 *
 * @author Bruno
 */
public class Comision {
    private final float importe, comision;

    public Comision(float importe, float comision) {
        this.importe = importe;
        this.comision = comision;
    }

    public float getImporte() {
        return importe;
    }

    public float getComision() {
        return comision;
    }

}
